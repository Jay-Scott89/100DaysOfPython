piano_keys = ["a", "b", "c", "d", "e", "f", "g"]
piano_tuple = ("do", "re", "mi", "fa", "so", "la", "ti")

print(piano_keys[2:5]) # slice from 2nd till 5th position
print(piano_keys[2:]) # slice everything after 2nd position
print(piano_keys[:5]) # slice everthing before 5th position
print(piano_keys[::2]) # skip every second item
print(piano_keys[::-1]) # revers the list

print(piano_tuple[2:5]) # slice from 2nd till 5th position
